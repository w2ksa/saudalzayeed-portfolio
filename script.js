// Current language state
let currentLang = "en"

// Theme toggle functionality
const themeToggle = document.getElementById("theme-toggle")
const body = document.body

// Initialize theme
const savedTheme = localStorage.getItem("theme") || "dark"
body.setAttribute("data-theme", savedTheme)
updateThemeToggleText(savedTheme)

themeToggle.addEventListener("click", () => {
  const currentTheme = body.getAttribute("data-theme")
  const newTheme = currentTheme === "dark" ? "light" : "dark"

  body.setAttribute("data-theme", newTheme)
  localStorage.setItem("theme", newTheme)
  updateThemeToggleText(newTheme)
})

function updateThemeToggleText(theme) {
  const icon = themeToggle.querySelector("i")
  const text = themeToggle.querySelector("span")

  if (theme === "dark") {
    icon.className = "fas fa-sun"
    text.textContent = "Light Mode"
  } else {
    icon.className = "fas fa-moon"
    text.textContent = "Dark Mode"
  }
}

// Language toggle functionality
const languageToggle = document.getElementById("language-toggle")

languageToggle.addEventListener("click", () => {
  currentLang = currentLang === "en" ? "ar" : "en"
  switchLanguage(currentLang)
})

function switchLanguage(lang) {
  // Update text content
  Object.keys(translations[lang]).forEach((key) => {
    const element = document.getElementById(key)
    if (element) {
      element.textContent = translations[lang][key]
    }
  })

  // Update job tasks
  const jobTasksList = document.getElementById("job-tasks")
  if (jobTasksList) {
    jobTasksList.innerHTML = ""
    jobTasksTranslations[lang].forEach((task) => {
      const li = document.createElement("li")
      li.textContent = task
      jobTasksList.appendChild(li)
    })
  }

  // Update project features
  const projectFeaturesList = document.getElementById("project-features")
  if (projectFeaturesList) {
    projectFeaturesList.innerHTML = ""
    projectFeaturesTranslations[lang].forEach((feature) => {
      const li = document.createElement("li")
      li.textContent = feature
      projectFeaturesList.appendChild(li)
    })
  }

  // Update vision list
  const visionList = document.getElementById("vision-list")
  if (visionList) {
    visionList.innerHTML = ""
    visionListTranslations[lang].forEach((item) => {
      const li = document.createElement("li")
      li.textContent = item
      visionList.appendChild(li)
    })
  }

  // Update body classes and direction
  if (lang === "ar") {
    body.classList.add("rtl")
    body.setAttribute("dir", "rtl")

    // Add Arabic class to specific elements
    document.querySelectorAll("h1, h2, h3, .section-title, .skill-card-title").forEach((el) => {
      el.classList.add("arabic")
    })
  } else {
    body.classList.remove("rtl")
    body.setAttribute("dir", "ltr")

    // Remove Arabic class
    document.querySelectorAll(".arabic").forEach((el) => {
      el.classList.remove("arabic")
    })
  }

  // Store language preference
  localStorage.setItem("language", lang)
}

// Initialize language on page load
document.addEventListener("DOMContentLoaded", () => {
  const savedLang = localStorage.getItem("language") || "en"
  if (savedLang !== "en") {
    currentLang = savedLang
    switchLanguage(currentLang)
  }
})

// Smooth scrolling for anchor links
document.querySelectorAll('a[href^="#"]').forEach((anchor) => {
  anchor.addEventListener("click", function (e) {
    e.preventDefault()
    const target = document.querySelector(this.getAttribute("href"))
    if (target) {
      target.scrollIntoView({
        behavior: "smooth",
        block: "start",
      })
    }
  })
})
